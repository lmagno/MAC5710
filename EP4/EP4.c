FILE* sfopen(char const *filename, char const *mode) {
    FILE *file;

    file = fopen(filename, mode);
    if(!file) {
        fprintf(stderr, "ERROR: Couldn't open file '%s'.\n", filename);
        exit(EXIT_FAILURE);
    }

    return file;
}


void input(char const *filename, Sequence **s1_ptr, Sequence **s2_ptr) {
    FILE *file;
    Sequence *s1, *s2;

    file = sfopen(filename, "r");

    s1 = sequence_read(file);
    s2 = sequence_read(file);

    fclose(file);

    *s1_ptr = s1;
    *s2_ptr = s2;
}


int main(int argc, char const *argv[]) {
    Sequence *s1, *s2;
    Grid *g;
    int match, mismatch, indel;

    match    =  1;
    mismatch = -1;
    indel    = -1;

    input(argv[1], &s1, &s2);

    g = grid_create(s1, s2);
    grid_print(g);

    grid_fill(g, match, mismatch, indel);
    grid_print(g);
    grid_printarrows(g);
    grid_free(g);

    return 0;
}
